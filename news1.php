        <p><b>МВД инициировало очередные изменения, касающиеся регистрации транспорта. Проект опубликован и подписан президентом  4 августа текущего, 2019 года. Все изменения вступили в силу с 1 января 2020. Какие перспективы ждут автомобилистов?.. Как будут реализованы некоторые положения нового закона, до сих пор непонятно не только обывателям, но и сотрудникам МРЭО ГИБДД ГАИ. Сегодня 13 января 2020 г. и специалисты АгентГаи успели разобраться в некоторых вопросах и наработать определённую статистику.</b>
               <b>По прописке</b>
<br>
Как указывают новые правила регистрации автомобилей в 2019, снова вернется привязка регистрационных знаков автомобиля к региону регистрации владельца.
<br>
До настоящего момента (с октября 2013 года) действовала иная практика – владелец получал номера по месту постановки на учет. Связь кода региона в номерном знаке и прописки «хозяина» была отменена, то есть житель Челябинска мог купить в Москве авто, зарегистрировать его там и получить столичные номера. Теперь же при регистрации в Московском регионе вы получите номер только по прописке в паспорте. Если же коренной москвич Игорь приобретет в Ростове-на-Дону легковушку, то номер на его автомобиле все равно будет с кодом Москвы.
<br>
Региональную привязку решено вернуть из-за сложностей отслеживания преступников. Инспектору ГИБДД визуально не удается определить принадлежность ТС к региону. Даже если на машине стоит Питерский номер, то это не означает, что она не приехала, например, из Крыма.
<br>
Реализация этого пункта ведётся достаточно жёстко. Конечно, никого не заставляют срочно менять регион государственного  регистрационного знака, если он не соответствует прописки. Но при любом обращение в ГИБДД (востановить ПТС или СТС, сохранить номера, зарегистрировать автомобиль...) регион ГРЗ будет исправен в соответствие с регистрацией собственника.
<br>
Даже те лица, которые сдали регистрационные знаки до вступления в силу новых правил (до 01.01.2020) не смогут получить их обратно, если их регистрация не совпадает с регионом ГРЗ.
<br>
<b>*Важно.</b> В такой ситуации государственный регистрационный не сгорает. Вы просто не можете его забрать до тех пор, пока зарегистрированы в другом регионе.
<br>
<b>*Выход.</b> Наши люди его всегда найдут. Менять регистрацию. Это сделать не очень просто, но учитывая, что за некоторые знаки уплачено больше миллиона приходится делать так...
<br>
<b>Главный по номерам</b>
<br>
<b>Новый закон</b> наделяет полномочиями по выдаче государственных номеров специализированные фирмы. ГИБДД будет только лишь назначать буквенно-цифровую комбинацию, по которой таблички и будут производиться. В Москве и области будут выдавать специализированные, уполномоченные организации. Новые правила регистрации автомобилей в 2019 разрешают собственнику самостоятельно выбирать компанию для заказа номерного знака. Здесь мы спешим предостеречь – доверяйте только компаниям, имеющим соответствующий статус.
<br>
В отдельно взятых регионах, например, на Чукотке, в Якутии, МРЭО ГИБДД такие функции сохранит. Это связано с тем, что техническое обеспечение в некоторых субъектах РФ недостаточное. Кроме того, законно зарегистрированных организаций, которые обеспечат потребность по производству номеров, мало или нет вовсе.
<br>
На данный момент, работа по выдаче гос знаков организована следующим образом: Если Вы ставите автомобиль на учёт в родном регионе. (Москвич ставит на учёт автомобиль в ГИБДД Москвы) то Вы можете заплатить пошлину на 2000р. больше и получить номера прямо "из окошка" ГАИ. А если Вы регистрируете автомобиль в чужом регионе. ГРЗ Вам присвоят, но физически не выдадут. За изготовлением государственных регистрационных знаков придётся обращаться в специализированную фирму. Важно помнить, что после регистрации управлять автомобилем без регистрационных знаков нельзя. За данное правонарушение предусмотренно наказание вплоть до лишения прав на пол года.
